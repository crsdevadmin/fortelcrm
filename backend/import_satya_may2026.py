"""
backend/import_satya_may2026.py
================================
Imports SATYA DISTRIBUTORS secondary sales report (May 2026) into the
SalesEntry table so that ROI calculations work automatically.

Source: SATYA DISTRIBUTORS Customer & Product Analysis 01/05/2026–31/05/2026
        generated by PROFITMAKER software (Daxinsoft Technologies, Hyd)

Usage:
    cd fortel-crm
    python -m backend.import_satya_may2026

How it works:
    1. For each customer in the report, match by phone number → doctor_id
    2. For each product, match by name (fuzzy) → product_id; auto-create if missing
    3. For each rep name, match to user → associate_id (fallback: None logged)
    4. Week is derived from invoice date (day 1-7=W1, 8-14=W2, 15-21=W3, 22+=W4)
    5. Inserts SalesEntry rows; skips duplicates (upserts quantity/value)

After running:
    - ROI screen will show actual May 2026 sales per doctor
    - Unmatched customers and reps are logged to satya_import_log.txt
"""

