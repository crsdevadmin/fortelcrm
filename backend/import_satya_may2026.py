# ── Helper: fuzzy name match ───────────────────────────────────────────────────
def similarity(a: str, b: str) -> float:
    return SequenceMatcher(None, a.lower(), b.lower()).ratio()


def find_doctor_by_phone(phone: str):
    if not phone:
        return None
    # Clean phone — keep only digits
    digits = re.sub(r'\D', '', phone)
    # Try last 10 digits
    last10 = digits[-10:] if len(digits) >= 10 else digits
    all_docs = db.query(Doctor).filter(Doctor.is_active == True).all()
    for doc in all_docs:
        if not doc.phone:
            continue
        doc_digits = re.sub(r'\D', '', doc.phone)[-10:]
        if doc_digits == last10:
            return doc
    return None


def find_doctor_by_name(name: str):
    all_docs = db.query(Doctor).filter(Doctor.is_active == True).all()
    best, best_score = None, 0.0
    for doc in all_docs:
        s = similarity(name, doc.name or "")
        if s > best_score:
            best_score = s
            best = doc
    return best if best_score >= 0.7 else None


def find_or_create_product(raw_name: str):
    canonical = PRODUCT_ALIASES.get(raw_name.upper().strip(), raw_name.strip())
    # Try exact match
    prod = db.query(Product).filter(Product.name.ilike(canonical)).first()
    if prod:
        return prod
    # Try partial match
    all_prods = db.query(Product).all()
    for p in all_prods:
        if similarity(canonical, p.name) >= 0.80:
            return p
    # Auto-create
    log(f"  [NEW PRODUCT] '{canonical}' not found in DB — creating with rate=0 (update manually)")
    prod = Product(name=canonical, pack_size=None, rate=0.0, is_active=True)
    db.add(prod)
    db.commit()
    db.refresh(prod)
    return prod


def find_user_by_rep_name(rep_name: str):
    if not rep_name or rep_name.upper() in ("COUNTER SALES", "DR.", ""):
        return None
    # Strip "DR " prefix for matching
    clean = re.sub(r'^DR\.?\s*', '', rep_name, flags=re.IGNORECASE).strip()
    all_users = db.query(User).filter(User.is_active == True).all()
    best, best_score = None, 0.0
    for u in all_users:
        # Match against full name or clean name
        for candidate in [u.name or "", (u.name or "").split()[-1]]:
            s = similarity(clean, candidate)
            if s > best_score:
                best_score = s
                best = u
    return best if best_score >= 0.65 else None


