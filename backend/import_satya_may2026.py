    unmatched_reps      = []
    inserted            = 0
    skipped             = 0
    total_value         = 0.0

    for entry in REPORT:
        cname  = entry["customer"]
        phone  = entry["phone"]
        rep    = entry["rep"]

        # ── Match doctor ─────────────────────────────────────────
        doctor = find_doctor_by_phone(phone)
        if not doctor:
            doctor = find_doctor_by_name(cname)
        if not doctor:
            log(f"\n  ✗ {cname} (ph: {phone or 'n/a'}) — not found in doctor database")
            unmatched_customers.append({"customer": cname, "phone": phone, "rep": rep})
            continue

        log(f"\n  {cname}")
        log(f"  → Matched to: {doctor.name}  (Rep: {rep or 'unassigned'})")

        # ── Match rep (user) ──────────────────────────────────────
        user = find_user_by_rep_name(rep)
        if not user:
            unmatched_reps.append({"customer": cname, "rep": rep})
            fallback_user_id = doctor.manager_id
            if fallback_user_id:
                log(f"  → Rep '{rep}' not matched — using doctor's manager as fallback")
        else:
            fallback_user_id = user.id

        if not fallback_user_id:
            log(f"  → No rep or manager found — skipping this customer")
            skipped += len(entry["invoices"])
            continue

        # ── Process each invoice line ─────────────────────────────
        # Group all invoices by product → monthly total (qty + value)
        # Distributor report is monthly, so we store one entry per doctor×product
        # with week=1 and source tag in remarks.
        monthly_totals = {}   # canonical_product_name → [total_qty, total_value]

        for (date_str, prod_raw, qty, value) in entry["invoices"]:
            canonical = PRODUCT_ALIASES.get(prod_raw.upper().strip(), prod_raw.strip())
            if canonical not in monthly_totals:
                monthly_totals[canonical] = [0.0, 0.0]
            monthly_totals[canonical][0] += qty
            monthly_totals[canonical][1] += value

        for canonical, (qty, value) in monthly_totals.items():
            product = find_or_create_product(canonical)

            # Check for existing monthly entry (week=1 = distributor monthly total)
            existing = db.query(SalesEntry).filter(
                SalesEntry.doctor_id    == doctor.id,
                SalesEntry.associate_id == fallback_user_id,
                SalesEntry.product_id   == product.id,
                SalesEntry.year         == YEAR,
                SalesEntry.month        == MONTH,
                SalesEntry.week         == 1,
            ).first()

            if existing:
                existing.quantity = qty      # overwrite with distributor figure
                existing.value    = value
                existing.remarks  = "SATYA DISTRIBUTORS — May 2026 (monthly total)"
                existing.is_approved = True
                db.commit()
                log(f"  ↻  {product.name}: {qty:.0f} units  ₹{value:,.2f}  (updated)")
                inserted += 1
                total_value += value
            else:
                entry_obj = SalesEntry(
                    doctor_id    = doctor.id,
                    associate_id = fallback_user_id,
                    product_id   = product.id,
                    year         = YEAR,
                    month        = MONTH,
                    week         = 1,
                    quantity     = qty,
                    value        = value,
                    remarks      = "SATYA DISTRIBUTORS — May 2026 (monthly total)",
                    is_approved  = True,
                    submitted_at = datetime.utcnow(),
                )
                db.add(entry_obj)
                db.commit()
                log(f"  ✓  {product.name}: {qty:.0f} units  ₹{value:,.2f}")
                inserted += 1
                total_value += value

    # ── Summary ───────────────────────────────────────────────────────────────
    log("\n" + "━" * 60)
    log("  IMPORT COMPLETE")
    log("━" * 60)
    log(f"  ✓ Sales entries saved   : {inserted}")
    log(f"  ✗ Entries skipped       : {skipped}")
    log(f"  Total value imported    : ₹{total_value:,.2f}")
    log(f"  Customers matched       : {len(REPORT) - len(unmatched_customers)} / {len(REPORT)}")
    log("")

    if unmatched_customers:
        log(f"  ⚠  {len(unmatched_customers)} customer(s) not found in your doctor database.")
        log("     Add them as doctors first, then re-run this script:")
        for u in unmatched_customers:
            phone_str = f"ph: {u['phone']}" if u["phone"] else "no phone"
            log(f"     • {u['customer']}  ({phone_str})  rep: {u['rep'] or 'unassigned'}")
        log("")

    if unmatched_reps:
        matched_set = set(u["rep"] for u in unmatched_reps if u["rep"])
        log(f"  ℹ  {len(matched_set)} rep name(s) from the report didn't match any user.")
        log("     Doctor's manager was used as fallback. Verify these:")
        for rep_name in sorted(matched_set):
            log(f"     • '{rep_name}'")

    # Write log file
    log_path = os.path.join(os.path.dirname(__file__), "satya_import_log.txt")
    with open(log_path, "w", encoding="utf-8") as f:
        f.write("\n".join(LOG_LINES))
    print(f"\nFull log written to: {log_path}")


